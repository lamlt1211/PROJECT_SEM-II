    private void btnThemKieuSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemKieuSanPhamActionPerformed
        // TODO add your handling code here:
        String makieu = txtMakieu.getText();
        String tenkieu = txtTenKieu.getText();
        KichCo c = (KichCo) cboKichCo.getSelectedItem();

        String err = "";
        if (makieu.length() == 0) {
            err += "-Mã Kiểu sản phẩm không được để trống";
        }
        if (tenkieu.length() == 0) {
            err += "\n-Tên kiểu sản phẩm không được để trống";
        }

        if (err.length() == 0) {
            KieuSanPham k = new KieuSanPham(makieu, tenkieu, c.getKichCo());
            boolean bl = new KieuSanPhamDAO().insertKieuSanPham(k);
            if (bl) {
                JOptionPane.showMessageDialog(this, "Thêm thành công");
                hienthi();
                loadKieuSp();
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi");
            }
        } else {
            JOptionPane.showMessageDialog(this, err);
        }


    }//GEN-LAST:event_btnThemKieuSanPhamActionPerformed

