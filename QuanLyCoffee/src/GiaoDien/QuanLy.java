    public static void hienthiban() {
        tableModelBan.setRowCount(0);
        Connection con;
        CallableStatement cstmt = null;
        ResultSet rs = null;

        con = Ket_Noi.open();
        try {
            cstmt = con.prepareCall("{call getAllBan}");
            rs = cstmt.executeQuery();
            while (rs.next()) {
                boolean status = rs.getBoolean("TrangThai");
                String tt = "";
                if (status == true) {
                    tt = "Trống";
                } else if (status == false) {
                    tt = "Đang Phục Vụ";
                }
                Object[] dt = {rs.getInt("MaBan"), rs.getString("TenBan"), tt};
                tableModelBan.addRow(dt);

            }
        } catch (SQLException ex) {
            Logger.getLogger(BanDAO.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            Ket_Noi.closeAll(con, cstmt, rs);
        }
    }
