    public void hienthisp() {
        
        conn = Ket_Noi.open();
        try {
            cstmt = conn.prepareCall("{call getallSP}");
            rs = cstmt.executeQuery();
            while(rs.next()){
                Object [] dt ={rs.getString("MaSanPham"),rs.getString("TenSanPham"),rs.getString("TenKieu"),rs.getString("KichCo"),rs.getFloat("GiaTien")};
                tableModel.addRow(dt);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DatHang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void hienthispc() {
        
        int sl = Integer.parseInt(txtSL.getText()+"");
       String ma = this.getMaSp();
         conn = Ket_Noi.open();
        try {
            pstmt = conn.prepareStatement("select sp.MaSanPham,sp.TenSanPham,ksp.MaKieu,ksp.TenKieu,sp.KichCo,sp.GiaTien from SanPham sp inner join KieuSanPham ksp on sp.MaKieu=ksp.MaKieu  where sp.MaSanPham ='"+ma+"'");
            rs = pstmt.executeQuery();
            while(rs.next()){
                Object [] dt ={rs.getString("MaSanPham"),rs.getString("TenSanPham"),rs.getString("MaKieu"),rs.getString("TenKieu"),rs.getString("KichCo"),rs.getFloat("GiaTien"),sl,rs.getFloat("GiaTien")*sl};
                tableModel1SPC.addRow(dt);
            }
        } catch (SQLException ex) {
            Logger.getLogger(DatHang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

