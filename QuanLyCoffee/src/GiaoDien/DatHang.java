    private void btnThemSPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemSPActionPerformed
        // TODO add your handling code here:
        int row = 0;
        int j =0;
        conn = Ket_Noi.open();
        int idBan = BanHangCoffee.getId();
        for (int i = 0; i < tblSPchon.getRowCount(); i++) {
            String maSanPham = tblSPchon.getValueAt(row, 0)+"";
            String maKieu = tblSPchon.getValueAt(row, 2)+"";
            int sl = Integer.parseInt(txtSL.getText()+"");
            String kichco = tblSPchon.getValueAt(row, 4)+"";
            Float gia = Float.parseFloat(tblSPchon.getValueAt(row, 5)+"");
            try {
                cstmt = conn.prepareCall("{call AddMon(?,?,?,?,?,?)}");
                cstmt.setInt(1, idBan);
                cstmt.setString(2, maKieu);
                cstmt.setString(3, maSanPham);
                cstmt.setInt(4, sl);
                cstmt.setString(5, kichco);
                cstmt.setFloat(6, gia);
                j = cstmt.executeUpdate();
                if(j>0){
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                    
                    BanHangCoffee.hienthi();
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Có lỗi");
                }
            } catch (SQLException ex) {
                Logger.getLogger(DatHang.class.getName()).log(Level.SEVERE, null, ex);
            }
            row++;
        }
        
        
    }//GEN-LAST:event_btnThemSPActionPerformed

