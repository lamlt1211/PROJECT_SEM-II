    private void tblViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewMouseClicked
        // TODO add your handling code here:
        int row = tblView.getSelectedRow();
        if (row >= 0 && row < tblView.getColumnCount()) {
            try {
                txtMaNhanVien.setText(tblView.getValueAt(row, 0) + "");
                txtUsername.setText(tblView.getValueAt(row, 1) + "");
                txtMatKhau.setText(tblView.getValueAt(row, 2) + "");
                txtTenNhanVien.setText(tblView.getValueAt(row, 3) + "");
                String ngaysinh = tblView.getValueAt(row, 4) + "";
                SimpleDateFormat ss = new SimpleDateFormat("yyyy-MM-dd");
                Date ns = ss.parse(ngaysinh);
                txtNgaySinh.setDate(ns);
                String gt = tblView.getValueAt(row, 5) + "";
                if (gt.equals("Nam")) {
                    rdoNam.setSelected(true);
                } else {
                    rdoNu.setSelected(true);
                }

                txtDiaChi.setText(tblView.getValueAt(row, 6) + "");
                txtSoDienThoai.setText(tblView.getValueAt(row, 7) + "");

                String gr = tblView.getValueAt(row, 8) + "";
                if (gr.equals("Admin")) {
                    rdoAdmin.setSelected(true);
                } else {
                    rdoEmployee.setSelected(true);
                }

            } catch (ParseException ex) {
                Logger.getLogger(QuanLyNhanVien.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_tblViewMouseClicked

