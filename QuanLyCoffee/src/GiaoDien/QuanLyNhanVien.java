    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        // TODO add your handling code here:
        String manhanvien = txtMaNhanVien.getText();
        String username = txtUsername.getText();
        String password = txtMatKhau.getText();
        String tennhanvien = txtTenNhanVien.getText();

        Date ngaysinh = txtNgaySinh.getDate();

        boolean gioitinh;
        if (rdoNam.isSelected()) {
            gioitinh = true;
        } else {
            gioitinh = false;
        }
        String diachi = txtDiaChi.getText();
        String sodienthoai = txtSoDienThoai.getText();
        boolean groupname;
        if (rdoAdmin.isSelected()) {
            groupname = true;
        } else {
            groupname = false;
        }

        String err = "";
        if (manhanvien.length() == 0) {
            err += "-Mã nhân viên không được để trống";
        }
        if (username.length() == 0) {
            err += "\n-Tên tài khoản không được để trống";
        }
        if (password.length() == 0) {
            err += "\n-Mật khẩu không được để trống";
        }

        if (tennhanvien.length() == 0) {
            err += "\n-Tên nhân viên không được để trống";
        }
        if (ngaysinh == null) {
            err += "\n-Ngày sinh không được để trống";
        }

        if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
            err += "\n-Giới tính không dược để trống";
        }
        if (diachi.length() == 0) {
            err += "\n-Địa chỉ không được để trống";
        }
        if (sodienthoai.length() == 0) {
            err += "\n-Số điện thoại không được để trống";
        }
        if (!rdoAdmin.isSelected() && !rdoEmployee.isSelected()) {
            err += "\n-Group name không dược để trống";
        }

        if (err.length() == 0) {
            NhanVien nv = new NhanVien(manhanvien, username, password, tennhanvien, ngaysinh, gioitinh, diachi, sodienthoai, groupname);
            boolean bl = new NhanVienDAO().insertNhanVien(nv);
            if (bl) {
                JOptionPane.showMessageDialog(this, "Thêm mới thành công");
                hienthi();
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi");
            }
        } else {

            JOptionPane.showMessageDialog(this, err);
        }


    }//GEN-LAST:event_btnThemMoiActionPerformed

    public void hienthi() {
        tableModel.setRowCount(0);
        List<NhanVien> allNhanVien = new NhanVienDAO().getAllNhanVien();
        for (NhanVien nv : allNhanVien) {
            Object[] dt = {nv.getMaNhanVien(), nv.getUsername(), nv.getPassword(), nv.getTenNhanVien(), nv.getNgaySinh(), nv.isGioiTinh() ? "Nam" : "Nữ", nv.getDiaChi(), nv.getSoDienThoai(), nv.isGR_name() ? "Admin" : "Employee"};
            tableModel.addRow(dt);
        }
    }
